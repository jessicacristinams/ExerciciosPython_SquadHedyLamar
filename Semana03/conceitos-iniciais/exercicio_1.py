# 1. Faça um Programa que peça dois números, realize as principais
# operações soma, subtração, multiplicação, divisão

num1 = int(input('Entre com o primeiro número:\n'))

num2 = int(input('Entre com o segundo número:\n'))

print('Soma:', num1 + num2, '\n')
print('Subtração:', num1 - num2, '\n')
print('Multiplicação:', num1 * num2, '\n')
print('Divisão:', num1 / num2, '\n')