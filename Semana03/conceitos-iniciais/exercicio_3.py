# 3. Faça um Programa que peça a quantidade de quilômetros, transforme
# em metros, centímetros e milímetros.

km = float(input('Informe a quantidade de quilômetros:\n'))

print('Metros:', int(km * 1000),'m\n')
print('Centímetros:', int(km * 100000),'cm\n')
print('Milímetros:', int(km * 1000000),'ml\n')
