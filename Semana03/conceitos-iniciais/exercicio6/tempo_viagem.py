# Distância total em km (você pode alterar este valor)

distancia_total = 1000

# Velocidades de viagem (km/h)

velocidade_aviao = 600

velocidade_carro = 100

velocidade_onibus = 80


# Cálculo do tempo de viagem para cada meio de transporte

tempo_aviao = distancia_total / velocidade_aviao

tempo_carro = distancia_total / velocidade_carro

tempo_onibus = distancia_total / velocidade_onibus

# Exibição dos resultados

print("Tempo de viagem para avião: ", tempo_aviao, "h")

print("Tempo de viagem para carro: ", tempo_carro, "h")

print("Tempo de viagem para ônibus: ", tempo_onibus, "h")